'''
Unindo strings de uma lista para formar uma única string.
'''

lista_msg = ['Nós', 'somos', 'poeira', 'das', 'estrelas']

#unindo strings com *
msg = '*'.join(lista_msg)
print(msg)
print()

#unindo strings com ~
msg = '~'.join(lista_msg)
print(msg)
print()

#unindo strings com espaço
msg = ' '.join(lista_msg)
print(msg)
print()
