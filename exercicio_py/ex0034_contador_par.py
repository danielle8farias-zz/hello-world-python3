'''
Conta números pares de 1 a 50.
'''

#laço for
#   variável 'i' é automaticamente inicializada em 0
#   enquanto i for menor do que 51
#   range(inicio, fim, passo)
#   passo de 2 em 2
#   fim do range deve ser maior +1 do valor que se quer chegar
for i in range(2, 51, 2):
    print(i, end=' -')
print()
